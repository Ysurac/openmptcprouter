static inline u16 bit_invert(u16 d)
{
	register u8 i;
	register u16 r;
	register u16 bit;

	r = 0;
	for (i = 0; i < 16; i++) {
		bit = d & (1 << i);
		if (bit != 0)
			r |= 1 << (15 - i);
	}
	return r;
}

